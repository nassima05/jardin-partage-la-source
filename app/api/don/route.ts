/*
|--------------------------------------------------------------------------
| IMPORTS
|--------------------------------------------------------------------------
*/

// Import NextResponse
// Permet de retourner une réponse API
import { NextResponse } from "next/server";

// Import Stripe
import Stripe from "stripe";



/*
|--------------------------------------------------------------------------
| INSTANCE STRIPE
|--------------------------------------------------------------------------
|
| Connexion Stripe avec la clé secrète
| stockée dans .env
|
*/
const stripe = new Stripe(
  process.env.STRIPE_SECRET_KEY as string
);



/*
|--------------------------------------------------------------------------
| ROUTE POST
|--------------------------------------------------------------------------
|
| Cette route :
|
| - reçoit le montant du don
| - crée une session Stripe Checkout
| - retourne une URL Stripe
|
*/
export async function POST(request: Request) {

  try {



    /*
    |--------------------------------------------------------------------------
    | RÉCUPÉRATION BODY
    |--------------------------------------------------------------------------
    |
    | Exemple :
    |
    | {
    |   montant: 20
    | }
    |
    */
    const body = await request.json();



    /*
    |--------------------------------------------------------------------------
    | CONVERSION MONTANT
    |--------------------------------------------------------------------------
    */
    const montant =
      Number(body.montant);



    /*
    |--------------------------------------------------------------------------
    | VÉRIFICATION MONTANT
    |--------------------------------------------------------------------------
    */
    if (!montant || montant < 1) {

      return NextResponse.json(
        {
          message:
            "Le montant minimum est de 1 €."
        },
        {
          status: 400
        }
      );
    }



    /*
    |--------------------------------------------------------------------------
    | CRÉATION SESSION STRIPE
    |--------------------------------------------------------------------------
    */
    const session =
      await stripe.checkout.sessions.create({



        /*
        |--------------------------------------------------------------------------
        | MOYEN PAIEMENT
        |--------------------------------------------------------------------------
        */
        payment_method_types: ["card"],



        /*
        |--------------------------------------------------------------------------
        | TYPE SESSION
        |--------------------------------------------------------------------------
        |
        | payment
        | = paiement unique
        |
        */
        mode: "payment",



        /*
        |--------------------------------------------------------------------------
        | PRODUIT STRIPE
        |--------------------------------------------------------------------------
        */
        line_items: [

          {

            price_data: {

              /*
              |--------------------------------------------------------------------------
              | MONNAIE
              |--------------------------------------------------------------------------
              */
              currency: "eur",



              /*
              |--------------------------------------------------------------------------
              | NOM PRODUIT
              |--------------------------------------------------------------------------
              */
              product_data: {

                name:
                  "Don - Jardin Partagé La Source",
              },



              /*
              |--------------------------------------------------------------------------
              | MONTANT
              |--------------------------------------------------------------------------
              |
              | Stripe utilise les centimes
              |
              | 10 €
              | => 1000
              |
              */
              unit_amount: Math.round(montant * 100),
            },



            /*
            |--------------------------------------------------------------------------
            | QUANTITÉ
            |--------------------------------------------------------------------------
            */
            quantity: 1,
          },
        ],



        /*
        |--------------------------------------------------------------------------
        | URL SUCCÈS
        |--------------------------------------------------------------------------
        |
        | Après paiement réussi
        |
        */
        success_url:
          "http://localhost:3000/",



        /*
        |--------------------------------------------------------------------------
        | URL ANNULATION
        |--------------------------------------------------------------------------
        |
        | Si utilisateur annule paiement
        |
        */
        cancel_url:
          "http://localhost:3000/don",
      });



    /*
    |--------------------------------------------------------------------------
    | RETOUR URL STRIPE
    |--------------------------------------------------------------------------
    */
    return NextResponse.json({

      url: session.url,
    });

  } catch (error) {



    /*
    |--------------------------------------------------------------------------
    | DEBUG TERMINAL
    |--------------------------------------------------------------------------
    */
    console.error(error);



    /*
    |--------------------------------------------------------------------------
    | ERREUR SERVEUR
    |--------------------------------------------------------------------------
    */
    return NextResponse.json(
      {
        message:
          "Erreur lors de la création du don."
      },
      {
        status: 500
      }
    );
  }
}